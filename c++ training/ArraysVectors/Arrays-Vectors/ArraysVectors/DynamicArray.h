#pragma once

template<typename T>
class TDynamicArray
{
private:
	int Size = 0;
	int Capacity = 1;
	T* Data = new T[Capacity];

public:
	//---------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
	TDynamicArray() {}
	//---------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
	~TDynamicArray()
	{
		delete[] Data;
		Data = nullptr;
	}
};
