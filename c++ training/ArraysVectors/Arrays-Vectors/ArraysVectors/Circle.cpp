#include "Circle.h"

//---------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
FCircle::FCircle(const float NewRadius) : Radius(NewRadius)
{
}
//---------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
FCircle::FCircle(const FCircle& Other) : Radius(Other.Radius)
{
}
//---------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
float FCircle::GetArea() const
{
	return Radius * Radius * 3.141592653589793238462643383279502884f;
}
//---------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
float FCircle::GetPerimeter() const
{
	return 2.f * Radius * 3.141592653589793238462643383279502884f;
}
//---------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
std::string FCircle::GetShapeName() const
{
	return "Circle";
}
//---------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
bool FCircle::operator==(const FCircle& Other) const
{
	return Radius == Other.Radius;
}
