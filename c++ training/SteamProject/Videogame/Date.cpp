#include "Date.h"

//---------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
FDate::FDate() : Day(1), Month(1), Year(2000)
{
}
//---------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
FDate::FDate(const int NewDay, const int NewMonth, const int NewYear) : Day(NewDay), Month(NewMonth), Year(NewYear)
{
}
//---------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
void FDate::SetDate(const int NewDay, const int NewMonth, const int NewYear)
{
	Day = NewDay;
	Month = NewMonth;
	Year = NewYear;
}
//---------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
int FDate::GetDay() const
{
	return Day;
}
//---------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
int FDate::GetMonth() const
{
	return Month;
}
//---------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
int FDate::GetYear() const
{
	return Year;
}
bool FDate::IsValid() const
{
	return Day > 0 && Day < 32 &&
		Month > 0 && Month < 13 &&
		Year > 1900 && Day < 2023;;
}
//---------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
std::string FDate::ToFormattedString() const
{
	return std::to_string(GetDay()) + "/" + std::to_string(GetMonth()) + "/" + std::to_string(GetYear());
}
