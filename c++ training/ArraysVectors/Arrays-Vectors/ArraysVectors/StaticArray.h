#pragma once

template<typename T, int N>
class TStaticArray
{
private:
	T Data[N];

public:
	//---------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
	int GetSize() const
	{
		return N;
	}
	//---------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
	T& operator[](const int Index)
	{
		return Data[Index];
	}
	//---------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
	const T& operator[](const int Index) const
	{
		return Data[Index];
	}
	//---------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
	T& Front()
	{
		return Data[0];
	}
	//---------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
	const T& Front() const
	{
		return Data[0];
	}
	//---------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
	T& Back()
	{
		return Data[N - 1];
	}
	//---------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
	const T& Back() const
	{
		return Data[N - 1];
	}
	//---------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
	T* GetData()
	{
		return Data;
	}
	//---------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
	const T* GetData() const
	{
		return Data;
	}
	//---------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
	bool IsEmpty() const
	{
		return N == 0;
	}
	//---------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
	void Fill(const T& Copy)
	{
		for (T& Item : Data)
		{
			Item = Copy;
		}
	}
	//---------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
	void Swap(TStaticArray<T, N>& Other)
	{
		for (int i = 0; i < N; i++)
		{
			T TemporaryItem = Data[i];
			Data[i] = Other[i];
			Other[i] = TemporaryItem;
		}
	}
};