#pragma once
#include "SinglyLinkedList.h"

template<typename T>
class TQueue
{
private:
	TSLList<T> List;

public:
	const T& Front() const
	{
		return List.GetHead();
	}
	//---------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
	T& Front()
	{
		return List.GetHead();
	}
	//---------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
	void Enqueue(const T& Item)
	{
		List.AddTail(Item);
	}
	//---------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
	void Dequeue()
	{
		List.Remove(0);
	}
	//---------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
	bool IsEmpty() const
	{
		return List.IsEmpty();
	}
};