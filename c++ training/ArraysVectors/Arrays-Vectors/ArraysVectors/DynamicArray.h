#pragma once

template<typename T>
class TDynamicArray
{
private:
	int Size = 0;
	int Capacity = 1;
	T* Data = new T[Capacity];

public:
	//---------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
	TDynamicArray() {}
	//---------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
	~TDynamicArray()
	{
		delete[] Data;
		Data = nullptr;
	}
	//---------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
	T& operator[](int Index)
	{
		return Data[Index];
	}
	//---------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
	const T& operator[](int Index) const
	{
		return Data[Index];
	}
	//---------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
	T& Front()
	{
		return Data[0];
	}
	//---------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
	const T& Front() const
	{
		return Data[0];
	}
	//---------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
	T& Back()
	{
		return Data[Size - 1];
	}
	//---------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
	const T& Back() const
	{
		return Data[Size];
	}
	//---------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
	T* GetData()
	{
		return &Data[0];
	}
	//---------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
	const T* GetData() const
	{
		return &Data[0];
	}
};
